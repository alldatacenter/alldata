package com.netease.arctic.spark.writer;

import org.apache.iceberg.DeleteFile;
import org.apache.iceberg.io.TaskWriter;
import org.apache.iceberg.io.WriteResult;
import org.apache.spark.sql.catalyst.InternalRow;
import org.apache.spark.sql.connector.write.DataWriter;
import org.apache.spark.sql.connector.write.WriterCommitMessage;

import java.io.IOException;

public class SimpleInternalRowDataWriter implements DataWriter<InternalRow> {
  final TaskWriter<InternalRow> writer;

  public SimpleInternalRowDataWriter(TaskWriter<InternalRow> writer) {
    this.writer = writer;
  }

  @Override
  public void write(InternalRow record) throws IOException {
    writer.write(record);
  }

  @Override
  public WriterCommitMessage commit() throws IOException {
    WriteResult result = writer.complete();
    return new WriteTaskCommit(result.dataFiles(), new DeleteFile[0]);
  }

  @Override
  public void abort() throws IOException {
    if (this.writer != null) {
      this.writer.abort();
    }
  }

  @Override
  public void close() throws IOException {
    if (this.writer != null) {
      writer.close();
    }
  }
}
